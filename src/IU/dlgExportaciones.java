    private void MensajeLabel() {
        DecimalFormat formato = new DecimalFormat("#,###");
        if (PanelCiente.isShowing()) {
            if(tbClientes.getRowCount() <= 0){
                etiMensaje.setText(" SIN REGISTROS.");
            }else{
               etiMensaje.setText("SE HAN CONTABILIZADO " + formato.format(tbClientes.getRowCount()) + " CLIENTES EN LA TABLA."); 
            }            
        }else if (PanelIVA.isShowing()) {
            if(tbActosGrabados.getRowCount() <= 0){
                etiMensaje.setText(" SIN REGISTROS.");
            }else{
               etiMensaje.setText("SE HAN CONTABILIZADO " + formato.format(tbActosGrabados.getRowCount()) + " ACTOS GRAVADOS EN LA TABLA."); 
            }            
        } else if (PanelCabecera.isShowing()) {
            if (tbVentaCabecera.getRowCount() <= 0) {
                etiMensaje.setText(" SIN COINCIDENCIAS.");
            }else if(tbVentaCabecera.getRowCount() == 1){
                etiMensaje.setText("SE HA CONTABILIZADO " + tbVentaCabecera.getRowCount() + " VENTA CABECERA EN LA TABLA.");
            }else {
                etiMensaje.setText("SE HAN CONTABILIZADO " + formato.format(tbVentaCabecera.getRowCount()) + " VENTAS CABECERAS EN LA TABLA.");
            }
        } else if (PanelDetalle.isShowing()) {
            if (tbVentaDetalle.getRowCount() <= 0) {
                etiMensaje.setText(" SIN COINCIDENCIAS.");
            }else if (tbVentaDetalle.getRowCount() == 1) {
                etiMensaje.setText("SE HA CONTABILIZADO " + tbVentaDetalle.getRowCount() + " VENTA DETALLE EN LA TABLA.");
            } else {
                etiMensaje.setText("SE HAN CONTABILIZADO " + formato.format(tbVentaDetalle.getRowCount()) + " VENTAS DETALLES EN LA TABLA.");
            }
        } else {
            etiMensaje.setText("");
        }
    }

