    private void btnFiltrar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrar3ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat myFecha = new SimpleDateFormat("yyyy/MM/dd");
        Date desde = null;
        Date hasta = null;
        try {
            desde = myFecha.parse(txtFechaFiltroD1.getText().trim());
            hasta = myFecha.parse(txtFechaFiltroH1.getText().trim());
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        if (txtFechaD1.getText().trim().isEmpty()) {
            Mensajes.informacion("Seleccione la \"Fecha desde\".");
        } else if (txtFechaH1.getText().trim().isEmpty()) {
            Mensajes.informacion("Seleccione la \"Fecha hasta\".");
        } else if (desde.after(hasta)) {
            Mensajes.informacion("La \"Fecha desde\" no puede ser posterior a la \"Fecha hasta\".");
        } else {
            CabecerasTablas.limpiarTablasVentaDetalle(tbVentaDetalle);
            controlCliente.listVDContaduria(tbVentaDetalle, txtFechaFiltroD1.getText().trim(), txtFechaFiltroH1.getText().trim());
            MensajeLabel();
        }
    }//GEN-LAST:event_btnFiltrar3ActionPerformed

