        Date desde = null;
        Date hasta = null;
        try {
            desde = myFecha.parse(txtFechaFiltroD.getText().trim());
            hasta = myFecha.parse(txtFechaFiltroH.getText().trim());
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        if (txtFechaD.getText().trim().isEmpty()) {
            Mensajes.informacion("Seleccione la \"Fecha desde\".");
        } else if (txtFechaH.getText().trim().isEmpty()) {
            Mensajes.informacion("Seleccione la \"Fecha hasta\".");
        } else if (desde.after(hasta)) {
            Mensajes.informacion("La \"Fecha desde\" no puede ser posterior a la \"Fecha hasta\".");
        } else {
            CabecerasTablas.limpiarTablasVentaCabecera(tbVentaCabecera);
            controlCliente.listVCContaduria(tbVentaCabecera, txtFechaFiltroD.getText().trim(), txtFechaFiltroH.getText().trim());
            MensajeLabel();
        }
